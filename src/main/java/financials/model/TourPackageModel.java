package financials.model;

public class TourPackageModel {
	
	private int tp_id;
	private int p_id;
	private String tp_name;
	private String tp_destination;
	private String tp_nod;
	private String tp_price;
	private String tp_desc;
	
	public int getTp_id() {
		return tp_id;
	}
	public void setTp_id(int tp_id) {
		this.tp_id = tp_id;
	}
	public int getP_id() {
		return p_id;
	}
	public void setP_id(int p_id) {
		this.p_id = p_id;
	}
	public String getTp_name() {
		return tp_name;
	}
	public void setTp_name(String tp_name) {
		this.tp_name = tp_name;
	}
	public String getTp_destination() {
		return tp_destination;
	}
	public void setTp_destination(String tp_destination) {
		this.tp_destination = tp_destination;
	}
	public String getTp_nod() {
		return tp_nod;
	}
	public void setTp_nod(String tp_nod) {
		this.tp_nod = tp_nod;
	}
	public String getTp_price() {
		return tp_price;
	}
	public void setTp_price(String tp_price) {
		this.tp_price = tp_price;
	}
	public String getTp_desc() {
		return tp_desc;
	}
	public void setTp_desc(String tp_desc) {
		this.tp_desc = tp_desc;
	}

}
